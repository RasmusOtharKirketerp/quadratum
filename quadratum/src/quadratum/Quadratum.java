package quadratum;

public class Quadratum {

	//private
	private int x,y;
	private int res;
	
	
	public Quadratum(int x, int y, int res) {
		super();
		this.x = x;
		this.y = y;
		this.res = res;
	}
	//public
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getRes() {
		return res;
	}
	public void setRes(int res) {
		this.res = res;
	}
	
	

}
